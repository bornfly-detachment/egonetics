db.serialize(() => {
  const sql = `
    INSERT OR REPLACE INTO hm_protocol
      (id, category, layer, human_char, ui_visual, machine_lang, notes, sort_order)
    VALUES (?, ?, ?, ?, ?, ?, ?, ?)
  `
  let pending = entries.length
  for (const e of entries) {
    const uiVisual = JSON.stringify({
      group: e.layer,
      component: e.id.replace('ui-', ''),
      demo_props: e.demo_props,
      description: e.description,
    })
    const machineLang = JSON.stringify({
      type: e.id.replace('ui-', ''),
      props: e.props,
    })
    db.run(sql,
      [e.id, 'ui-component', e.layer, e.human_char, uiVisual, machineLang, e.description, e.sort],
      function(err) {
        if (err) console.error('❌', e.id, err.message)
        else     console.log('✅', e.id, `[${e.layer}]`)
        if (--pending === 0) db.close()
      }
    )
  }
})
